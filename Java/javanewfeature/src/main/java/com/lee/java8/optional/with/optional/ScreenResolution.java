package com.lee.java8.optional.with.optional;

/**
 * @author litz-a
 */
public class ScreenResolution {

  private int width;
  private int height;

  public ScreenResolution(int width, int height) {
    this.width = width;
    this.height = height;
  }

  public int getWidth() {
    return width;
  }

  public int getHeight() {
    return height;
  }

}
