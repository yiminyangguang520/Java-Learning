package com.lee.java9.reactive.ex3;

/**
 * @author litz-a
 */
public class JavaArticle {

  private int id;
  private String title;

  public JavaArticle(int id, String title) {
    this.id = id;
    this.title = title;
  }

  public int getId() {
    return id;
  }

  public String getTitle() {
    return title;
  }
}
